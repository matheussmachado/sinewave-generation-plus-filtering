/* USER CODE BEGIN 4 */
void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim)
{
	// VARYING DAC FREQUENCY
	// =============================================================================
	if (htim->Instance == TIM2) {
		x[0] = (float32_t) adc_dma[1];
		dac_val = HAL_DAC_GetValue(&hdac, DAC_CHANNEL_1);
		count++;
		if (count >= COUNTS_PER_PERIOD) {
			count = 0;
			freq_i++;
			if (freq_i >= (int)freq_len) {
				freq_i = 0;
			}
			__HAL_TIM_SET_AUTORELOAD(&htim2, (uint32_t)(1000/freq[freq_i]) - 1);
			frequency = freq[freq_i];
		}
	}
	// =============================================================================
	// PERFORMING IIR FILTER
	// =============================================================================
	if (htim->Instance == TIM4) {
		arm_biquad_cascade_df1_f32(&s1, x, y, (uint32_t)BLOCK_SIZE);
	}
	// =============================================================================
}

